public class AreaOfCircle {
    public static void main(String[] args){
        double radius;
        double area;
        radius = 5;
        // Step 1: Read in radius
        
        // Step 2: Compute Area
        area = (radius * radius) * 3.14159;

        // Step 3: Display the Area
        System.out.println("The area for the circle of raius " + radius + " is " + area);
    }
}
